# Program 2: Prime Numbers Using List

n = int(input("Enter the limit: "))

prime_list = []

for num in range(2, n + 1):

    is_prime = True

    for i in range(2, num):
        if num % i == 0:
            is_prime = False
            break

    if is_prime:
        prime_list.append(num)

print("Prime numbers up to", n, "are:")
print(prime_list)




